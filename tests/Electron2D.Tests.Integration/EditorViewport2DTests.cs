using System.Diagnostics;
using Xunit;

namespace Electron2D.Tests.Integration;

public sealed class EditorViewport2DTests
{
    [Fact]
    public async Task Viewport2DSmokeRunCoversNavigationSelectionTransformsOverlaysAndCameraPreview()
    {
        var root = FindRepositoryRoot();
        var projectPath = Path.Combine(root, "src", "Electron2D.Editor", "Electron2D.Editor.csproj");
        var workRoot = CreateTemporaryDirectory("electron2d-editor-viewport-2d-");

        try
        {
            var startInfo = new ProcessStartInfo
            {
                FileName = "dotnet",
                WorkingDirectory = root,
                RedirectStandardOutput = true,
                RedirectStandardError = true
            };
            startInfo.ArgumentList.Add("run");
            startInfo.ArgumentList.Add("--project");
            startInfo.ArgumentList.Add(projectPath);
            startInfo.ArgumentList.Add("--");
            startInfo.ArgumentList.Add("--viewport-2d-smoke");
            startInfo.ArgumentList.Add(workRoot);

            using var process = Process.Start(startInfo) ?? throw new InvalidOperationException("Failed to start dotnet run.");
            var outputTask = process.StandardOutput.ReadToEndAsync();
            var errorTask = process.StandardError.ReadToEndAsync();

            await process.WaitForExitAsync();
            var output = await outputTask;
            var error = await errorTask;

            Assert.True(
                process.ExitCode == 0,
                $"2D Viewport smoke run failed with exit code {process.ExitCode}.{Environment.NewLine}stdout:{Environment.NewLine}{output}{Environment.NewLine}stderr:{Environment.NewLine}{error}");

            var lines = ParseMachineReadableOutput(output);

            Assert.Contains("Electron2D.Editor 2D viewport smoke passed", output);
            Assert.Equal("-120,-140", lines["Pan"]);
            Assert.Equal("2", lines["Zoom"]);
            Assert.Equal("Player|Enemy", lines["Selected"]);
            Assert.Equal("160,0", lines["PlayerPosition"]);
            Assert.Equal("160,200", lines["EnemyPosition"]);
            Assert.Equal("90", lines["PlayerRotation"]);
            Assert.Equal("90", lines["EnemyRotation"]);
            Assert.Equal("2,2", lines["PlayerScale"]);
            Assert.Equal("2,2", lines["EnemyScale"]);
            Assert.Equal("140,-20,40,240", lines["SelectionBounds"]);
            Assert.Equal("1", lines["CollisionOverlays"]);
            Assert.Equal("0,0,400,300", lines["CameraPreview"]);
            Assert.Equal("True", lines["WorldUnderCursorStable"]);
        }
        finally
        {
            Directory.Delete(workRoot, recursive: true);
        }
    }

    private static Dictionary<string, string> ParseMachineReadableOutput(string output)
    {
        return output.Split(Environment.NewLine, StringSplitOptions.RemoveEmptyEntries)
            .Select(line => line.Split('=', 2))
            .Where(parts => parts.Length == 2)
            .ToDictionary(parts => parts[0], parts => parts[1], StringComparer.Ordinal);
    }

    private static string CreateTemporaryDirectory(string prefix)
    {
        var directory = Path.Combine(Path.GetTempPath(), prefix + Guid.NewGuid().ToString("N"));
        Directory.CreateDirectory(directory);
        return directory;
    }

    private static string FindRepositoryRoot()
    {
        var directory = new DirectoryInfo(AppContext.BaseDirectory);

        while (directory is not null)
        {
            if (File.Exists(Path.Combine(directory.FullName, "src", "Electron2D.sln")))
            {
                return directory.FullName;
            }

            directory = directory.Parent;
        }

        throw new DirectoryNotFoundException("Electron2D repository root was not found.");
    }
}
